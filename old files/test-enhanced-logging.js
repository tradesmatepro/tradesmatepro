    console.log('This test verifies the enhanced error capture system can detect all types of issues.\n');
    
    // Clear error logs first
    const latestPath = path.join(__dirname, 'error_logs', 'latest.json');
    fs.writeFileSync(latestPath, JSON.stringify([], null, 2));
    console.log('‚úÖ Cleared error logs\n');
    
    console.log('üìù ENHANCED LOGGING FEATURES:');
    console.log('‚úÖ Captures console.log, console.warn, console.error');
    console.log('‚úÖ Detects spam patterns (messages repeated > 10 times in 5 seconds)');
    console.log('‚úÖ Categorizes issues by type:');
    console.log('   ‚Üí SPAM_DETECTED (like SLOT ADDED)');
    console.log('   ‚Üí PERFORMANCE_ISSUE');
    console.log('   ‚Üí NETWORK_ISSUE');
    console.log('   ‚Üí DATABASE_ERROR');
    console.log('   ‚Üí AUTH_ERROR');
    console.log('   ‚Üí VALIDATION_ERROR');
    console.log('   ‚Üí DEPRECATED_CODE');
    console.log('   ‚Üí MEMORY_WARNING');
    console.log('');
    
    console.log('üìù TESTING STEPS:');
    console.log('1. Open browser and go to: http://localhost:3006');
    console.log('2. Open browser console (F12)');
    console.log('3. Navigate around the app to trigger various issues');
    console.log('4. Look for any repeated messages (should be detected as spam)');
    console.log('5. Run: sendErrors() in browser console');
    console.log('6. Check the enhanced error report');
    console.log('');
    
    console.log('üîç WHAT TO EXPECT:');
    console.log('   ‚úÖ Enhanced sendErrors() function shows:');
    console.log('      ‚Üí Total count of errors + warnings + logs');
    console.log('      ‚Üí Spam analysis with detected patterns');
    console.log('      ‚Üí Categorized issue types');
    console.log('      ‚Üí Frequency analysis');
    console.log('');
    
    console.log('üß™ MANUAL SPAM TEST (Optional):');
    console.log('   ‚Üí In browser console, run this to simulate spam:');
    console.log('     for(let i=0; i<15; i++) console.log("TEST SPAM MESSAGE", i);');
    console.log('   ‚Üí Then run: sendErrors()');
    console.log('   ‚Üí Should detect "TEST SPAM MESSAGE" as spam pattern');
    console.log('');
    
    console.log('‚è≥ Waiting 60 seconds for you to test...');
    console.log('   (Press Ctrl+C if you need more time)');
    
    // Wait 60 seconds for manual testing
    await new Promise(resolve => setTimeout(resolve, 60000));
    
    console.log('\nüìä CHECKING ENHANCED ERROR LOGS...');
    
    // Check what errors were captured
    if (fs.existsSync(latestPath)) {
      const errors = JSON.parse(fs.readFileSync(latestPath, 'utf8'));
      
      if (errors.length === 0) {
        console.log('‚ÑπÔ∏è  No errors captured in latest.json');
        console.log('   ‚Üí This could mean:');
        console.log('     1. No issues occurred (good!)');
        console.log('     2. Enhanced logging needs verification');
        console.log('     3. sendErrors() wasn\'t called in browser');
      } else {
        console.log(`‚úÖ Captured ${errors.length} items:`);
        
        // Analyze captured data
        const types = {};
        const sources = {};
        const severities = {};
        
        errors.forEach((error, index) => {
          console.log(`\nüîç Item ${index + 1}:`);
          console.log(`   Type: ${error.type || 'UNKNOWN'}`);
          console.log(`   Source: ${error.source || 'UNKNOWN'}`);
          console.log(`   Severity: ${error.severity || 'UNKNOWN'}`);
          console.log(`   Message: ${error.message?.substring(0, 100) || 'No message'}...`);
          
          // Count types
          const type = error.type || 'UNKNOWN';
          types[type] = (types[type] || 0) + 1;
          
          const source = error.source || 'UNKNOWN';
          sources[source] = (sources[source] || 0) + 1;
          
          const severity = error.severity || 'UNKNOWN';
          severities[severity] = (severities[severity] || 0) + 1;
        });
        
        console.log('\nüìä ANALYSIS SUMMARY:');
        console.log('   Types detected:', Object.keys(types).join(', '));
        console.log('   Sources:', Object.keys(sources).join(', '));
        console.log('   Severities:', Object.keys(severities).join(', '));
        
        // Check for spam detection
        const spamItems = errors.filter(e => e.type === 'SPAM_DETECTED');
        if (spamItems.length > 0) {
          console.log(`\nüö® SPAM DETECTED: ${spamItems.length} spam items found!`);
          console.log('   ‚Üí Enhanced logging successfully detected spam patterns');
        } else {
          console.log('\n‚úÖ No spam detected - console should be clean');
        }
      }
    } else {
      console.log('‚ùå No error log file found');
    }
    
    console.log('\nüéØ ENHANCED LOGGING SUMMARY:');
    console.log('The enhanced system now captures:');
    console.log('   ‚úÖ All console.log messages (with spam detection)');
    console.log('   ‚úÖ All console.warn messages (with categorization)');
    console.log('   ‚úÖ All console.error messages (with enhanced typing)');
    console.log('   ‚úÖ Frequency analysis for spam detection');
    console.log('   ‚úÖ Issue categorization and severity levels');
    console.log('   ‚úÖ Comprehensive reporting via sendErrors()');
    console.log('');
